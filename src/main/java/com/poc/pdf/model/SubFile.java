package com.poc.pdf.model;


public class SubFile extends ToString {
    private static final long serialVersionUID = 4413197980635604318L;

    private String name;

    private String length;

    private Long width;

    private Long height;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLength() {
        return length;
    }

    public void setLength(String length) {
        this.length = length;
    }

    public Long getWidth() {
        return width;
    }

    public void setWidth(Long width) {
        this.width = width;
    }

    public Long getHeight() {
        return height;
    }

    public void setHeight(Long height) {
        this.height = height;
    }
}